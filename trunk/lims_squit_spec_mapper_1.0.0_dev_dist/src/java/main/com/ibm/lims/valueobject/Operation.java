package com.ibm.lims.valueobject;


/**
 * <p>
 * This enumeration class represents the operation.
 * It contains NONE, Multiple, Divide operation values.
 * </p>
 * <p>
 * Thread-safety: Enumeration class is thread-safe.
 * </p>
 * 
 * @author phead
 * @version 1.0
*/
public class Operation {
    /**
     * <p>
     * Represents the NONE operation.
     * </p>
    */
    public static Operation NONE;

    /**
     * <p>
     * Represents the Multiply operation.
     * </p>
    */
    public static Operation MULTIPLY;

    /**
     * <p>
     * Represents the Divide operation.
     * </p>
    */
    public static Operation DIVIDE;
}
